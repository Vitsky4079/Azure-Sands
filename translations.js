// Detect which page we're on
function getPageKey() {
  const path = window.location.pathname;
  if (path.includes('contact'))     return 'contact';
  if (path.includes('location'))    return 'location';
  if (path.includes('unit-studio')) return 'studio';
  if (path.includes('unit-1bed'))   return '1bed';
  if (path.includes('unit-2bed'))   return '2bed';
  if (path.includes('units'))       return 'units';
  if (path.includes('remont'))      return 'remont';
  return 'index';
}

function applyTranslations(lang) {
  const t = window.TRANSLATIONS[lang];
  if (!t) return;

  // Plain text
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // HTML content (contains tags like <br>, <em>)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // Page title
  const pageKey = getPageKey();
  const titleKey = 'page.title.' + pageKey;
  if (t[titleKey]) document.title = t[titleKey];

  // Month abbreviations in sea temp chart
  const monthEls = document.querySelectorAll('[data-month-idx]');
  if (monthEls.length && t['months']) {
    const months = t['months'].split(',');
    monthEls.forEach(el => {
      const idx = parseInt(el.getAttribute('data-month-idx'), 10);
      if (months[idx] !== undefined) el.textContent = months[idx];
    });
  }

  // Update navbar flag & code
  const flagEl = document.getElementById('langFlag');
  const codeEl = document.getElementById('langCode');
  if (flagEl) flagEl.textContent = LANG_META[lang].flag;
  if (codeEl) codeEl.textContent = LANG_META[lang].code;

  // Mark active item in dropdown
  document.querySelectorAll('.lang-menu li').forEach(li => {
    li.classList.toggle('active', li.getAttribute('data-lang') === lang);
  });
}

function setLanguage(lang) {
  if (!window.TRANSLATIONS[lang]) return;
  localStorage.setItem('az_lang', lang);
  applyTranslations(lang);
  closeLangMenu();
}

function toggleLangMenu() {
  const menu = document.getElementById('langMenu');
  if (menu) menu.classList.toggle('open');
}

function closeLangMenu() {
  const menu = document.getElementById('langMenu');
  if (menu) menu.classList.remove('open');
}

// Close on outside click
document.addEventListener('click', e => {
  const switcher = document.getElementById('langSwitcher');
  if (switcher && !switcher.contains(e.target)) closeLangMenu();
});

// Init on load
document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('az_lang') || 'pl';
  applyTranslations(saved);
});
